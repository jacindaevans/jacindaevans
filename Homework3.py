# functions vs methods#
# methods refer to a function that is part of a class, and it can be accessed by an object or instance of that class#
# all methods are functions, but all functions aren't methods#

def function_name(function_argument_1, function_argument_2):
    multiplied = function_argument_1 * function_argument_2
    return multiplied


print(function_name(5, 10))


def function_name(function_argument_1, function_argument_2):
    divided = function_argument_1 / function_argument_2
    return divided


print(function_name(5, 10))


def function_name(function_argument_1, function_argument_2):
    subtracted = function_argument_1 - function_argument_2
    return subtracted


print(function_name(5, 10))


def function_name(function_argument_1, function_argument_2):
    added = function_argument_1 + function_argument_2
    return added


print(function_name(5, 10))


